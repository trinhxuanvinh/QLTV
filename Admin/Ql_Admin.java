    public void ShowTable() {
    DefaultTableModel model = (DefaultTableModel) tb.getModel();
    model.setRowCount(0);

    try {
        ketnoiqltv pt = new ketnoiqltv();
        ResultSet rs = pt.getData("sach");
        
        // Ensure model has correct columns (add if necessary)
        if (model.getColumnCount() == 0) {
            model.setColumnIdentifiers(new String[]{
                "MaSach", "TenSach", "TacGia", "TheLoai", 
                "NhaXuatBan", "NamXuatBan", "ISBN", 
                "SoLuong", "ViTri", "TrangThai", "MaNhaCungCap"
            });
        }

        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getString("MaSach"),
                rs.getString("TenSach"),
                rs.getString("TacGia"),
                rs.getString("TheLoai"),
                rs.getString("NhaXuatBan"),
                rs.getString("NamXuatBan"),
                rs.getString("ISBN"),
                rs.getString("SoLuong"),
                rs.getString("ViTri"),
                rs.getString("TrangThai"),
                rs.getString("MaNhaCungCap")
            });
        }
    } catch (SQLException | ClassNotFoundException e) {
        e.printStackTrace(); // Proper error logging
    }
}
