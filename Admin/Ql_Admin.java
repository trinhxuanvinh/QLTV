    public void DeleteSelectedRow() throws ClassNotFoundException, SQLException {
    int selectedRow = tb.getSelectedRow();
    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(null, "Vui lòng chọn dòng cần xóa.");
        return;
    }

    int confirm = JOptionPane.showConfirmDialog(null, "Bạn có chắc chắn muốn xóa sách này?", "Xác nhận", JOptionPane.YES_NO_OPTION);
    if (confirm != JOptionPane.YES_OPTION) {
        return;
    }

    int maSach = Integer.parseInt(tb.getValueAt(selectedRow, 0).toString());

    ketnoiqltv kn;
    try {
        kn = new ketnoiqltv();
        kn.deleteSach(maSach);
    } catch (SQLException sqlException) {
        sqlException.printStackTrace();
    } catch (ClassNotFoundException ex) {
        Logger.getLogger(Ql_Admin.class.getName()).log(Level.SEVERE, null, ex);
    }
}
