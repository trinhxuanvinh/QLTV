    private void btnxoaqltlActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnxoaqltlActionPerformed
        try {
        int selectedRow = tb.getSelectedRow();
        if (selectedRow != -1) {
            // Có dòng được chọn trong bảng → dùng DeleteSelectedRow
            DeleteSelectedRow();
            ShowTable();
            JOptionPane.showMessageDialog(null, "Đã xóa thành công (qua bảng)");
        } else if (!txtMaSach.getText().trim().isEmpty()) {
            // Không chọn dòng, nhưng có nhập mã sách → dùng Delete
            Delete();
            ShowTable();
            JOptionPane.showMessageDialog(null, "Đã xóa thành công (qua mã sách)");
        } else {
            // Không chọn dòng và cũng không nhập mã
            JOptionPane.showMessageDialog(null, "Vui lòng chọn dòng hoặc nhập mã sách để xóa.");
        }
    } catch (ClassNotFoundException | SQLException ex) {
        Logger.getLogger(Ql_Admin.class.getName()).log(Level.SEVERE, null, ex);
        JOptionPane.showMessageDialog(null, "Lỗi khi xóa: " + ex.getMessage());
    }
    }//GEN-LAST:event_btnxoaqltlActionPerformed
    
