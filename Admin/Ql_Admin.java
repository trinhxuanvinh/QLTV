    public void DeleteNhanVienFromTable() throws ClassNotFoundException, SQLException {
    int selectedRow = tbnv.getSelectedRow();
    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(null, "Vui lòng chọn dòng cần xóa.");
        return;
    }

    int confirm = JOptionPane.showConfirmDialog(null, "Bạn có chắc chắn muốn xóa nhân viên này?", "Xác nhận", JOptionPane.YES_NO_OPTION);
    if (confirm != JOptionPane.YES_OPTION) {
        return;
    }
    int maNV = Integer.parseInt(tbnv.getValueAt(selectedRow, 0).toString());
    ketnoiqltv kn;
    try {
        kn = new ketnoiqltv();
        kn.deleteNhanVien(maNV);
    } catch (SQLException sqlException) {
        sqlException.printStackTrace();
    } catch (ClassNotFoundException ex) {
        Logger.getLogger(Ql_Admin.class.getName()).log(Level.SEVERE, null, ex);
    }
}

    private void btnxoaqlnvActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnxoaqlnvActionPerformed
        try {
        int selectedRow = tbnv.getSelectedRow();
        if (selectedRow != -1) {
            // Có dòng được chọn trong bảng
            DeleteNhanVienFromTable();
            ShowTBNV(); // cập nhật lại bảng
            JOptionPane.showMessageDialog(null, "Đã xóa thành công (qua bảng)");
        } else if (!txtMaNV.getText().trim().isEmpty()) {
            // Có nhập mã nhân viên
            DeleteNhanVien();
            ShowTBNV(); // cập nhật lại bảng
            JOptionPane.showMessageDialog(null, "Đã xóa thành công (qua mã)");
        } else {
            JOptionPane.showMessageDialog(null, "Vui lòng chọn dòng hoặc nhập mã nhân viên để xóa.");
        }
    } catch (ClassNotFoundException | SQLException ex) {
        Logger.getLogger(Ql_Admin.class.getName()).log(Level.SEVERE, null, ex);
        JOptionPane.showMessageDialog(null, "Lỗi khi xóa: " + ex.getMessage());
    }
    }//GEN-LAST:event_btnxoaqlnvActionPerformed
